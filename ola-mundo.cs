//Imprime cada uma em uma linha
Console.WriteLine("Hello ");
Console.WriteLine("world!");


Console.WriteLine("\n");

//	\n - pula linha
Console.WriteLine("Hello \nworld!");


Console.WriteLine("\n");

//	\t - espaçamento(tab)
Console.WriteLine("Hello \tworld!");


Console.WriteLine("\n");

//	\\ - utilizado quando se quer printar a barra, as barras duplas printam apenas uma \ no resultado.
Console.WriteLine("Hello\\world!");


Console.WriteLine("\n");

//	"\"\ - para utilizar aspas dentro de uma frase a ser impressa.
Console.WriteLine("Hello \"New\" world!");


Console.WriteLine("\n");

//	@ - antes das aspas dentro do colsole, deixa a impressão no mesmo formato que a declaração no Console.
Console.WriteLine(@"Hello 	
			world!");


Console.WriteLine("\n");

//	\u - seguido do código de caracteres de outras linguas, seguindo a tabela unicode.
Console.WriteLine("\u0048\u0065\u006c\u006c\u006f world!");